"""
screener/lib/storage/schema.py — Collection constants, doc-ID builders, and
Pydantic document schemas for the multi-agent stock screener.

Collection layout (mirrors the private reference implementation):

    tickers/                              — per-ticker signal data
    tickers/{SYMBOL}/memory/{MONTH_ID}    — per-ticker episodic memory (subcollection)
    signals/     — monthly raw signal snapshots written by financial_update_job
    picks/       — monthly screener top-N snapshots (screener_picks)
    performance/ — monthly P&L snapshots and pick ledger
    chunks/      — EDGAR 10-K/10-Q text chunks with vector embeddings (disclosure_chunks)
    eval/        — monthly eval output (evals_output)
    events/      — pipeline lifecycle events (screener_events)

Doc ID conventions:
    - {TICKER}                          → tickers master record (e.g. "AAPL")
    - screening_{YYYYMMDD}              → legacy daily snapshot (unused in write path)
    - {TICKER}_{YYYY-MM}                → signals docs (e.g. "AAPL_2026-04")
    - picks_{YYYYWW}                    → picks snapshot (legacy; not used in write path)
    - perf_{YYYYWW}                     → performance snapshots
    - {TICKER}_{YYYY-MM}_{source}       → pick ledger entries (e.g. "AAPL_2026-04_judge")
    - {MONTH_ID}                        → memory docs (e.g. "2026-04", one per month per ticker)
    - chunk_{TICKER_SLUG}_{IDX}         → EDGAR chunks
    - eval_{YYYYMM}                     → eval docs
    - event_{UUID}                      → event docs (auto-generated by Firestore)

Memory subcollection path helper:
    memory_collection_path(ticker)  → "tickers/AAPL/memory"
    memory_doc_id(month_id)         → "2026-04"
"""

from __future__ import annotations

from datetime import datetime, timezone
from typing import Optional

from pydantic import BaseModel, Field


# ---------------------------------------------------------------------------
# Collection name constants
# ---------------------------------------------------------------------------

TICKERS: str = "tickers"
MEMORY: str = "memory"
SIGNALS: str = "signals"
PICKS: str = "picks"
PERFORMANCE: str = "performance"
SCREENINGS: str = "screenings"
CHUNKS: str = "chunks"
EVAL: str = "eval"
EVENTS: str = "events"


# ---------------------------------------------------------------------------
# Doc ID helper functions
# ---------------------------------------------------------------------------


def ticker_to_slug(ticker: str) -> str:
    """Canonical slug used in Firestore doc IDs.

    Lower-cases the ticker and replaces ``-`` and ``.`` with ``_`` so that
    symbols like ``BRK.B`` and ``BF-B`` produce valid, predictable IDs.

    Examples::

        ticker_to_slug("AAPL")   → "aapl"
        ticker_to_slug("BRK.B")  → "brk_b"
        ticker_to_slug("BF-B")   → "bf_b"
    """
    return ticker.lower().replace("-", "_").replace(".", "_")


def screening_doc_id(date_str: str) -> str:
    """Doc ID for a daily ticker-signals snapshot.

    Args:
        date_str: ISO date string, e.g. ``"2026-04-29"``.

    Returns:
        e.g. ``"screening_20260429"``
    """
    return f"screening_{date_str.replace('-', '')}"


def picks_doc_id(week_id: str) -> str:
    """Doc ID for a weekly picks snapshot.

    Args:
        week_id: ISO week string, e.g. ``"202618"``.

    Returns:
        e.g. ``"picks_202618"``
    """
    return f"picks_{week_id}"


def perf_snapshot_doc_id(week_id: str) -> str:
    """Doc ID for a weekly performance snapshot.

    Args:
        week_id: ISO week string, e.g. ``"202618"``.

    Returns:
        e.g. ``"perf_202618"``
    """
    return f"perf_{week_id}"


def performance_doc_id(month_id: str, source: str = "judge") -> str:
    """Doc ID for a monthly performance snapshot in the ``performance`` collection.

    Args:
        month_id: Month identifier in ``"YYYY-MM"`` format, e.g. ``"2026-04"``.
        source: Agent that produced the verdict; defaults to ``"judge"``.

    Returns:
        e.g. ``"2026-04_judge"``
    """
    return f"{month_id}_{source}"


def pick_ledger_doc_id(ticker: str, month_id: str, source: str = "judge") -> str:
    """Doc ID for an individual pick-ledger entry in the ``picks`` collection.

    Args:
        ticker: Upper-case ticker symbol, e.g. ``"AAPL"``.
        month_id: Month identifier in ``"YYYY-MM"`` format, e.g. ``"2026-04"``.
        source: Agent that produced the verdict; defaults to ``"judge"``.

    Returns:
        e.g. ``"AAPL_2026-04_judge"``
    """
    return f"{ticker.upper()}_{month_id}_{source}"


def memory_collection_path(ticker: str) -> str:
    """Firestore path for a ticker's episodic memory subcollection.

    The memory subcollection lives under the ticker's top-level document:
    ``tickers/{SYMBOL}/memory``.  Pass this as the ``collection`` argument
    to :meth:`StorageDAO.get` / :meth:`StorageDAO.set`.

    Args:
        ticker: Ticker symbol (any case).

    Returns:
        e.g. ``"tickers/AAPL/memory"``
    """
    return f"{TICKERS}/{ticker.upper()}/{MEMORY}"


def memory_doc_id(month_id: str) -> str:
    """Doc ID for a monthly episodic memory document.

    One document per month per ticker, keyed by ``YYYY-MM`` month identifier.
    The document lives at ``tickers/{SYMBOL}/memory/{MONTH_ID}``.

    Args:
        month_id: Month identifier in ``"YYYY-MM"`` format, e.g. ``"2026-04"``.

    Returns:
        The month_id unchanged, e.g. ``"2026-04"``
    """
    return month_id


def ticker_doc_id(ticker: str) -> str:
    """Doc ID for a master ticker record in the ``tickers`` collection.

    The ticker symbol is used directly (upper-cased) as the Firestore doc ID
    so that ``tickers/AAPL`` is the stable reference for all subcollections
    (e.g. ``tickers/AAPL/memory``).

    Args:
        ticker: Ticker symbol (any case).

    Returns:
        Upper-cased ticker symbol, e.g. ``"AAPL"``
    """
    return ticker.upper()


def signal_doc_id(ticker: str, month_id: str) -> str:
    """Doc ID for a monthly raw-signal snapshot in the ``signals`` collection.

    Args:
        ticker: Upper-case ticker symbol, e.g. ``"AAPL"``.
        month_id: Month identifier in ``"YYYY-MM"`` format, e.g. ``"2026-04"``.

    Returns:
        e.g. ``"AAPL_2026-04"``
    """
    return f"{ticker.upper()}_{month_id}"


def current_month_id(dt: datetime | None = None) -> str:
    """Return the month ID string for a datetime, e.g. ``"2026-04"``.

    Args:
        dt: Datetime to use; defaults to ``datetime.now(timezone.utc)``.
    """
    if dt is None:
        dt = datetime.now(timezone.utc)
    return dt.strftime("%Y-%m")


def chunk_doc_id(ticker: str, index: int) -> str:
    """Doc ID for an EDGAR text chunk.

    Args:
        ticker: Ticker symbol (any case).
        index: Zero-based chunk index within the filing.

    Returns:
        e.g. ``"aapl_42"``
    """
    return f"{ticker_to_slug(ticker)}_{index}"


def eval_doc_id(year: int, month: int) -> str:
    """Doc ID for a monthly eval output document.

    Args:
        year: Four-digit year.
        month: Month number (1–12).

    Returns:
        e.g. ``"eval_202604"``
    """
    return f"eval_{year:04d}{month:02d}"


def screening_run_doc_id(month_id: str) -> str:
    """Doc ID for a monthly screening run document in the ``screenings`` collection.

    The month identifier is used directly as the document ID so that
    ``screenings/2026-04`` is the stable reference for the full scoring run
    output for April 2026.

    Args:
        month_id: Month identifier in ``"YYYY-MM"`` format, e.g. ``"2026-04"``.

    Returns:
        The month_id unchanged, e.g. ``"2026-04"``
    """
    return month_id


def current_week_id(dt: datetime | None = None) -> str:
    """Return the ISO week ID string for a datetime, e.g. ``"202618"``.

    Args:
        dt: Datetime to use; defaults to ``datetime.now(timezone.utc)``.
    """
    if dt is None:
        dt = datetime.now(timezone.utc)
    return dt.strftime("%G%V")


def current_quarter_id(dt: datetime | None = None) -> str:
    """Return the quarter ID string for a datetime, e.g. ``"2026Q2"``.

    Args:
        dt: Datetime to use; defaults to ``datetime.now(timezone.utc)``.
    """
    if dt is None:
        dt = datetime.now(timezone.utc)
    q = (dt.month - 1) // 3 + 1
    return f"{dt.year}Q{q}"


